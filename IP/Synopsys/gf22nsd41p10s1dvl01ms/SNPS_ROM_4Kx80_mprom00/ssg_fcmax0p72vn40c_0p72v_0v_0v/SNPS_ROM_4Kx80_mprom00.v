function is_adr_Valid;  // 1-bit return value
input [11:0] addr_to_check;
integer addr_bit_count;
reg     ret_value;
  begin
    ret_value = `True;
      for (addr_bit_count = 0; addr_bit_count <= 11; addr_bit_count = addr_bit_count +1)
        if ((addr_to_check[addr_bit_count] !== 1'b0) && (addr_to_check[addr_bit_count] !== 1'b1))
        begin
          flaga_adr_x = `True;
          ret_value = `False;
        end
      if(addr_to_check > 4095)
      begin
        ret_value = `False;
      end
   is_adr_Valid = ret_value;
  end
endfunction


function is_rm_Valid;
input [3:0] rm_to_chk;
integer rm_bit_cnt;
reg ret_val;
  begin
    ret_val = `True;
    for (rm_bit_cnt = 0; rm_bit_cnt < 4; rm_bit_cnt = rm_bit_cnt + 1)
    if ( (rm_to_chk[rm_bit_cnt] !== 1'b0) && (rm_to_chk[rm_bit_cnt] !== 1'b1) )
      ret_val = `False;
      is_rm_Valid = ret_val;
  end
endfunction

